    Thread clock1 = new Thread() {

        @Override
        public void run() {
            int hor = 00, min = 00, seg = 00;

            for (;;) {
                try {
                    //System.out.println(hor + ":" + min + ":" + seg);
                    seg++;
                    if (seg > 59) {
                        seg = 0;
                        min++;
                    }
                    if (min > 59) {
                        seg = 0;
                        min = 0;
                        hor++;
                    }
                    if (min == 2) {
                        dispose();
                    }
                    tempo.setText(hor + ":" + min + ":" + seg);
                    Thread.sleep(999);
                } catch (InterruptedException e) {

                }
            }
        }
    };

